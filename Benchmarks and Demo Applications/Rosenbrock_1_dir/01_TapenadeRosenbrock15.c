void rosenbrock(float x[MAX_DIR], float *res){
	float a = 1.0f;
	float b = 100.0f;
	for(int i = 0; i < MAX_DIR-1; i++){
		*res = *res + (b*((x[i+1]-x[i]*x[i])*(x[i+1]-x[i]*x[i]))+((a-x[i])*(a-x[i])));
	}
}


/*
  Differentiation of rosenbrock in forward (tangent) mode:
   variations   of useful results: *res
   with respect to varying inputs: x[0:MAX_DIR-1]
   RW status of diff variables: res:(loc) *res:out x:(loc) x[0:MAX_DIR-1]:in
   Plus diff mem management of: res:in x:in
*/
void rosenbrock_dv(float x[MAX_DIR], float xd[MAX_DIR][NBDirsMax], float *res, float (*resd)[NBDirsMax], int nbdirs) {
    float a;
    int nd;
    a = 1.0f;
    float b;
    b = 100.0f;
    for (nd = 0; nd < NBDirsMax; ++nd)
        (*resd)[nd] = 0.0;
    {
      float temp;
      for (int i = 0; i < MAX_DIR-1; ++i) {
          temp = x[i + 1] - x[i]*x[i];
          for (nd = 0; nd < nbdirs; ++nd)
              (*resd)[nd] = (*resd)[nd] + b*2*temp*(xd[i+1][nd]-2*x[i]*xd[i][nd]) - 2*(a-x[i])*xd[i][nd];
          *res = *res + b*(temp*temp) + (a-x[i])*(a-x[i]);
      }
    }
}

bool check_abort(float (*resd)[NBDirsMax]){
	int check_abort = 0;
	bool abort = false;
	for (int i = 0; i < MAX_DIR; i++) {
		float grad = (*resd)[i];
		if(fabsf(grad) < STOP){
			check_abort++;
		}
		if(isnanf(grad)){
			abort = true;
			break;
		}
	}
	if(check_abort >= MAX_DIR-1){
		abort = true;
	}
	return abort;
}

float optimize(void){
	int itercounter = 0;
	float x[MAX_DIR];
	float xd[MAX_DIR][NBDirsMax];
	float res = 0.0f;
	float resd[NBDirsMax];
	float change[MAX_DIR];
	for(int i = 0; i < MAX_DIR; i++){
		x[i] = 0.0f;
		resd[i] = 0.0f;
		change[i] = 0.0f;
		for(int j = 0; j < MAX_DIR; j++){
			xd[i][j] = 0.0f;
		}
	}
	for (int i = 0; i < MAX_DIR; i++){
		xd[i][i] = 1.0f;
	}

	bool abort = false;
	while((abort == false) && (itercounter < MAX_ITER)){
		//reset res for new result
		res = 0.0f;
		rosenbrock_dv(x, xd, &res, &resd, MAX_DIR);
		for (int i = 0; i < MAX_DIR; i++) {
			float grad = resd[i];
			change[i] = (grad * STEP) + (MOMENTUM * change[i]);
			x[i] = x[i] - change[i];
			//NEW SEEDING NOT REQUIRED; SEEDS OF X SHOULD NOT CHANGE!
    	}
		abort = check_abort(&resd);
		itercounter++;
	}

	for (int i = 0; i < MAX_DIR; i++){
#ifdef PRINT
		printf("x: %f \n", x[i]);
		//printf("xd: %f \n", xd[i][i]):
		printf("resd: %f\n", resd[i]);
#else
 		store_in_fpos(&x[i], i+1);
#endif
	}
#ifdef PRINT
	printf("itercounter: %d \n", itercounter);
	printf("result: %f \n", res);
#else
 	store_in_pos(&itercounter, MAX_DIR+1);
#endif
	return res;
}

